# This assumes that (unformatted) html doc has been generated in the docs/ directory, and has not been comitted to git.
git add docs && \
git stash push -m "Autogenerated documentation for publishing on github pages" -- docs && \
git switch github-pages && \
git merge --allow-unrelated-histories -s recursive -X theirs refs/stash && git stash drop
for f in docs/*.html 
  do pandoc -s --template docs/da-style/da-pandoc.html --metadata pagetitle="Contingent Claims" -o $f $f 
done
git add docs

